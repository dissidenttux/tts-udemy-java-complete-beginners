/* 9. ForLoops */
public class LoopsFor {
    public static void main (String[] args) {
		
		/* For Loop
		 * for(initializing statment; conditional statement; incremental statement)*/
		for(int i = 0; i<5; i++) {
			/* printf = format specifier. "%d" in this situation is basically "replade %d with the specificer (i) after
			 * the comma.
			 * The escape character with n "/n" means write on newline */
			System.out.printf("The value of i is: %d\n", i);
		}
    }
} 
